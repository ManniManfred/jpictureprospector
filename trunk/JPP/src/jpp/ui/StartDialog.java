    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
      String fehler = null;
      
      /* Eingaben ueberpruefen */
      if (this.arbeiteLokal()) {
        File ordner = new File(this.getIndex());
        
        if (ordner.exists()) {
          if (!ordner.isDirectory()) {
            fehler = "Bei der angegeben Datei handelt es sich nicht um "
                    + "ein Verzeichnis."
                    + "\nBitte waehlen Sie ein Verzeichnis aus.";
          } else {
            /* Alles OK, das Verzeichnis kann verwendet werden */
          }
        } else {
          /* Falls der Ordner nicht existiert, versuch diesen automatisch 
           * anzulegen
           */
          if (ordner.mkdirs()) {
            /* Alles OK, der Ordner wurde erzeugt und kann nun verwendet werden 
             */
          } else {
            /* Der Ordner konnte nicht angelegt werden */
            fehler = "Das angegebene Verzeichnis existiert nicht und konnte "
                    + "nicht und konnte nicht erzeugt werden."
                    + "\nBitte waehlen Sie ein anderes Verzeichnis aus.";
          }
        }
        
       
      } else {
         if (!JPPClient.istJPPServerVerfuegbar(getJPPServer())) {
            fehler = "Der angegebene Server ist "
                + "nicht verfuegbar.\n Meldung vom Server: " 
                + JPPClient.getMeldungVomServer()
                + "\nBitte geben Sie einen verf√ºgbaren Server an." 
                + "\nBeachten Sie, dass die URL mit einem \"/\" schliessen" 
                + " muss." 
                + "\nBeispiel: http://localhost:8180/jpp/";
         }
      }
      
      
      /* Falls keine Fehler aufgetreten sind, diesen Dialog schliessen.
       * Andernfalls die entsprechende Fehlermedlung anzeigen
       */
      if (fehler == null) {
        doClose(RET_OK);
      } else {
        JOptionPane.showMessageDialog(this, fehler);
      }
    }//GEN-LAST:event_okButtonActionPerformed

